    private void btnRestaurarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRestaurarActionPerformed
        try {
            JOptionPane.showMessageDialog(this, "Ao fim da restauração, o aplicativo será FINALIZADO!");
            JFileChooser fileChooser = new JFileChooser();
            if (rbtDados.isSelected()) {
                fileChooser.setFileFilter(new FileNameExtensionFilter("BackUp", "backup"));
            } else {
                fileChooser.setFileFilter(new FileNameExtensionFilter("ZIP", "zip"));
            }
            int result = fileChooser.showOpenDialog(this);
            File file = null;
            if (result == JFileChooser.APPROVE_OPTION) {
                file = fileChooser.getSelectedFile();
            }

            if (file != null) {
                boolean sucesso;
                if (rbtDados.isSelected()) {
                    sucesso = Backup.restoreBD(file.getAbsolutePath());
                } else {
                    sucesso = Zip.extrairZipArquivoUnico(file, new File("."), "./TranSOFT.backup");
                    if (sucesso) {
                        sucesso = Backup.restoreBD("TranSOFT.backup");
                    }
                    if (sucesso) {
                        sucesso = Zip.extrairZip(file, new File("."));
                    }
                    new File("TranSOFT.backup").delete();
                    if (sucesso) {
                        System.exit(0);
                    }
                }
                if (sucesso) {
                    JOptionPane.showMessageDialog(this, "Restauração efetuada com Sucesso!");
                    System.exit(0);
                } else {
                    JOptionPane.showMessageDialog(this, "Erro ao restaurar Backup!");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Operação cancelada pelo usuário!");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Erro ao restaurar Backup!");
            Logger.getLogger(IfrBackup.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnRestaurarActionPerformed

    private void btnBackupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackupActionPerformed
        try {
            JFileChooser fileChooser = new JFileChooser();
            fileChooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
            fileChooser.setApproveButtonText("Selecionar");
            int result = fileChooser.showOpenDialog(this);
            File file = null;
            if (result == JFileChooser.APPROVE_OPTION) {
                file = fileChooser.getSelectedFile();
            }

            if (file != null) {
                boolean sucesso = false;
                String dataHora = Formatacao.getDataHoraAtualArquivo();
                if (rbtDados.isSelected()) {
                    sucesso = Backup.backupBD(file.getAbsolutePath() + "\\TranSOFT " + dataHora + ".backup");
                } else {
                    if (file.getAbsolutePath().equals(new File("").getAbsolutePath())) {
                        JOptionPane.showMessageDialog(this, "Destino do Backup não pode ser na pasta de instalação!");
                    } else {
                        sucesso = Backup.backupBD("TranSOFT.backup");
                        if (sucesso) {
                            Zip.zipFolder(".", file.getAbsolutePath() + "\\TranSOFT " + dataHora + ".zip");
                        }
                    }
                }
                if (sucesso) {
                    JOptionPane.showMessageDialog(this, "Backup efetuado com Sucesso!");
                } else {
                    JOptionPane.showMessageDialog(this, "Erro ao efetuar Backup!");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Operação cancelada pelo usuário!");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Erro ao efetuar Backup!");
            Logger.getLogger(IfrBackup.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnBackupActionPerformed


