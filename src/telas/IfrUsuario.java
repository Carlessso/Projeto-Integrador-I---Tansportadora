    private String validaUsuario() {
        String erro = "";

        if (tfdLogin.isEnabled()) {
            if (UsuarioDao.loginUsado(tfdLogin.getText().toLowerCase())) {
                erro = "Login em uso \n";
            }
        }
        if (new String(pwdSenha.getPassword()).length() < 6) {
            erro = erro + "Sua senha deve ter no mínimo seis caracteres \n";
        }

        if (!new String(pwdSenha.getPassword()).equals(new String(pwdConfirmarSenha.getPassword()))) {
            erro = erro + "Senhas com valores diferentes \n";
        }

        return erro;
    }

    private void resetaCampos() {
        tfdNome.setText("");
        tfdLogin.setText("");
        pwdSenha.setText("");
        pwdConfirmarSenha.setText("");
        chkAtivo.setSelected(true);
        pessoaSelecionada = null;
        usuario = new Usuario();
        tfdLogin.setEnabled(true);
    }

