    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        try {
            this.resetaCampos();
            int idUsuario = Integer.parseInt(String.valueOf(tblUsuario.getValueAt(tblUsuario.getSelectedRow(), 0)));
            Usuario u = UsuarioDao.buscaId(idUsuario);

            pessoaSelecionada = u.getPessoa();
            usuario = u;
            tfdNome.setText(pessoaSelecionada.getNome());
            tfdLogin.setText(u.getLogin());
            pwdSenha.setText(u.getSenha());
            pwdConfirmarSenha.setText(u.getSenha());
            chkAtivo.setSelected(u.isAtivo());
            tfdLogin.setEnabled(false);
            
            jTabbedPane1.setSelectedIndex(1);
        } catch (Exception e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(this, "Escolha a pessoa a ser EDITADA!");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

