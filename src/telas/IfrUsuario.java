    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        String erro = validaUsuario();

        if (erro.length() == 0) {
            usuario.setLogin(tfdLogin.getText().toLowerCase());
            usuario.setSenha(new String(pwdSenha.getPassword()));
            usuario.setAtivo(chkAtivo.isSelected());
            usuario.setPessoa(pessoaSelecionada);

            if (Dao.salvar(usuario).equals("Sucesso")) {
                JOptionPane.showMessageDialog(this, "Usuário cadastrado com sucesso!");
                this.resetaCampos();
                jTabbedPane1.setSelectedIndex(0);
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao cadastrar pessoa!");
            }
        } else {
            JOptionPane.showMessageDialog(this, erro);
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

