    private void btnSelecionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelecionarActionPerformed
        try {
            this.resetaCampos();
            int idPessoa = Integer.parseInt(String.valueOf(tblPessoa.getValueAt(tblPessoa.getSelectedRow(), 0)));
            pessoaSelecionada = PessoaDao.buscaId(idPessoa);

            if (!UsuarioDao.pessoaUsada(pessoaSelecionada)) {
                pessoaSelecionada = PessoaDao.buscaId(idPessoa);
                tfdNome.setText(pessoaSelecionada.getNome());
                jTabbedPane1.setSelectedIndex(1);
            } else {
                JOptionPane.showMessageDialog(this, "Esta pessoa já possui cadastro!");
                pessoaSelecionada = null;
            }
        } catch (Exception e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(this, "Escolha a pessoa!");
        }
    }//GEN-LAST:event_btnSelecionarActionPerformed

