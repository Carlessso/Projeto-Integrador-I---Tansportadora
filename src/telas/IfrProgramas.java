    private void btnDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletarActionPerformed
        try {
            int idProgrmaa = Integer.parseInt(String.valueOf(tblProgramas.getValueAt(tblProgramas.getSelectedRow(), 0)));
            Programas p;

            p = ProgramaDao.buscaId(idProgrmaa);

            int result = JOptionPane.showConfirmDialog(null, "Deseja Excluir ? ", "Excluir", JOptionPane.YES_NO_CANCEL_OPTION);

            if (result == JOptionPane.YES_OPTION) {
                Dao.deletar(p);
                ProgramaDao.popularTabelaFiltro(tblProgramas, "", "descricao");
            }

            tblProgramas.clearSelection();
        } catch (Exception e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(this, "Escolha o programa a ser deletado!");
        }
    }//GEN-LAST:event_btnDeletarActionPerformed

