    private void btnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarActionPerformed
        if (tfdDescricao.getText().equals("") || tfdDescricao.getText().length() < 3)
        {
            JOptionPane.showMessageDialog(this, "Preencha um nome válido para a Tela!");
            return;
        }
        
        Programas p = new Programas();
        
        if(tblProgramas.getSelectedRowCount() > 0)
        {
            this.id = (int) tblProgramas.getValueAt(tblProgramas.getSelectedRow(), 0);
            tfdId.setText(String.valueOf(id));
            p = ProgramaDao.buscaId(id);            
        }
        p.setDescricao(tfdDescricao.getText());
        
        if (Dao.salvar(p).equals("Sucesso")) {
            this.id = 0;
            JOptionPane.showMessageDialog(this, "Programa cadastrado com sucesso!");
            tfdDescricao.setText("");
            tfdId.setText("");
            ProgramaDao.popularTabelaFiltro(tblProgramas, "", "descricao");
            tblProgramas.clearSelection();
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao cadastrar programa!");
        }
    }//GEN-LAST:event_btnGravarActionPerformed

